package model;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertEquals;

public class CoordinatesTest {

    @Test
    public void testDistanceSamePoint() {
        DistanceCalculator calc = new DistanceCalculator();
        Coordinates p1 = new Coordinates(0.0, 0.0);
        Coordinates p2 = new Coordinates(0.0, 0.0);
        double distance = calc.getDistance(p1.getX(), p1.getY(), p2.getX(), p2.getY());
        assertEquals(0.0, distance, 0.001, "Distance should be 0 fot he same points");
    }


    @Test
    public void testDistanceDifferentPoint() {
        DistanceCalculator calc = new DistanceCalculator();
        Coordinates p1 = new Coordinates(0.0, 0.0);
        Coordinates p2 = new Coordinates(3.0, 4.0);
        double distance = calc.getDistance(p1.getX(), p1.getY(), p2.getX(), p2.getY());
        assertEquals(5.0, distance, 0.001, "Distance between (0.0) and (3,4) should be 5");
    }
}
