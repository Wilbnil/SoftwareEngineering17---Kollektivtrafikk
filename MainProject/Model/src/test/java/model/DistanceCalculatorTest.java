package org.gruppe17.kollektivtrafikk.model;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertEquals;

public class DistanceCalculatorTest {

    @Test
    public void testCalculatorTravelTime() {
        DistanceCalculator calc = new DistanceCalculator();
        double distance = calc.getDistance(0.0, 0.0, 3.0, 0.0);
        double time = calc.calculateTravelTime(distance, 60.0);
        assertEquals(3.0, time, 0.001, "Travel time for 3 km ay 60 km/h should be 3 minutes.");
    }
}
